// QUEUE FIRST-IN-FIRST-OUT (FIFO)

A = [];


// QUEUE HEAD
A.push("Leo");
A.push("John");
A.push("Peter");
A.push("Priya");
//QUEUE TAIL

console.log(A); //PRINT TO THE CONSOLE THE QUEUE ARRAY

//ENQUEUE HEAD
A.shift();
A.shift();
A.shift();
//...........

console.log(A); //PRINT TO THE CONSOLE THE ENQUEUED ARRAY
